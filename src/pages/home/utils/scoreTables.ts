// 根据小分和难度级别查找分数（支持插值）
export function getScore(
  rawScore: number,
  scoreTable: number[],
  supportsHalf: boolean = false
): number {
  // 确保分数在有效范围内
  if (rawScore < 0) rawScore = 0;
  if (rawScore > scoreTable.length - 1) rawScore = scoreTable.length - 1;

  // 如果是整数，直接返回
  if (!supportsHalf || rawScore === Math.floor(rawScore)) {
    const index = Math.floor(rawScore);
    return scoreTable[scoreTable.length - 1 - index];
  }

  // 支持0.5分插值计算
  const lower = Math.floor(rawScore);
  const upper = Math.ceil(rawScore);
  const lowerIndex = scoreTable.length - 1 - lower;
  const upperIndex = scoreTable.length - 1 - upper;

  const lowerScore = scoreTable[lowerIndex];
  const upperScore = scoreTable[upperIndex];

  // 线性插值
  const ratio = rawScore - lower;
  return Math.round(lowerScore + (upperScore - lowerScore) * ratio);
}

